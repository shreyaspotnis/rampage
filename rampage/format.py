def bold(s):
    return '<b>'+s+'</b>'


def italic(s):
    return '<i>'+s+'</i>'


def b(s):
    return bold(s)


def i(s):
    return italic(s)


def red(s):
    return '<font color="red">'+s+'</font>'


def green(s):
    return '<font color="green">'+s+'</font>'


def blue(s):
    return '<font color="blue">'+s+'</font>'

